package result;

import java.text.SimpleDateFormat;
import java.util.Date;

public class ResultVO {
	
	private int gameNo;
	private int myTeamNo;
	private int rivalTeamNo;
	private int myRuns;
	private int myHits;
	private int myError;
	private int myBalls;
	private String rivalTeam;
	private int rivalRuns;
	private int rivalHits;
	private int rivalError;
	private int rivalBalls;
	private Date gameDate;
	
	public ResultVO() {}

	public int getGameNo() {
		return gameNo;
	}

	public void setGameNo(int gameNo) {
		this.gameNo = gameNo;
	}

	public int getMyTeamNo() {
		return myTeamNo;
	}

	public void setMyTeamNo(int myTeamNo) {
		this.myTeamNo = myTeamNo;
	}

	public int getRivalTeamNo() {
		return rivalTeamNo;
	}

	public void setRivalTeamNo(int rivalTeamNo) {
		this.rivalTeamNo = rivalTeamNo;
	}

	public int getMyRuns() {
		return myRuns;
	}

	public void setMyRuns(int myRuns) {
		this.myRuns = myRuns;
	}

	public int getMyHits() {
		return myHits;
	}

	public void setMyHits(int myHits) {
		this.myHits = myHits;
	}

	public int getMyError() {
		return myError;
	}

	public void setMyError(int myError) {
		this.myError = myError;
	}

	public int getMyBalls() {
		return myBalls;
	}

	public void setMyBalls(int myBalls) {
		this.myBalls = myBalls;
	}

	public String getRivalTeam() {
		return rivalTeam;
	}

	public void setRivalTeam(String rivalTeam) {
		this.rivalTeam = rivalTeam;
	}

	public int getRivalRuns() {
		return rivalRuns;
	}

	public void setRivalRuns(int rivalRuns) {
		this.rivalRuns = rivalRuns;
	}

	public int getRivalHits() {
		return rivalHits;
	}

	public void setRivalHits(int rivalHits) {
		this.rivalHits = rivalHits;
	}

	public int getRivalError() {
		return rivalError;
	}

	public void setRivalError(int rivalError) {
		this.rivalError = rivalError;
	}

	public int getRivalBalls() {
		return rivalBalls;
	}

	public void setRivalBalls(int rivalBalls) {
		this.rivalBalls = rivalBalls;
	}

	public Date getGameDate() {
		return gameDate;
	}

	public String getStrDate() {
		
		SimpleDateFormat sdf = new SimpleDateFormat("yyyy.MM.dd hh:mm:ss");
		return sdf.format(gameDate);
	}
	
	public void setGameDate(Date gameDate) {
		this.gameDate = gameDate;
	}
	
	

	
	
	

}
